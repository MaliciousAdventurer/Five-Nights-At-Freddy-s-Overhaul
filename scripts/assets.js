function loadAssets(){
    for(var i = 0; i < allSprites.length; i++){
        var spriteLength = allSprites[i].length //amount of sprites inside
        for(var j = 0; j < spriteLength; j++){
            var constImg = new Image() 
            constImg.src = allSprites[i][j]
            elem("spriteSheet").appendChild(constImg)
        }
    }

    allSprites = null

    elem("menu").style.display = "block"

    $("#menu_warning").fadeOut(4000);
    $("#menu_warningbackground").fadeOut(4000);

    animate("static_1", fullStaticAsset, true);
    playSound("menuStatic", menuStatic, 0.08, true)

    setTimeout(() => {
        $("#menu_newgame").animate({
            top: "45vh"
        }, 1500)
    }, 1000);
    setTimeout(() => {
        $("#menu_continue").animate({
            top: "55vh"
        }, 1500)
    }, 1250);
    if(ls.getItem("FNAFREVAMPED_night") == "5"){
        setTimeout(() => {
            $("#menu_night6").animate({
                top: "65vh"
            }, 1500)
        }, 1750);
    }
    if(ls.getItem("FNAFREVAMPED_night6victory") == "true"){
        setTimeout(() => {
            $("#menu_night7").animate({
                top: "75vh"
            }, 1500)
        }, 2000);
    }
}

